x=10
y=20
# and operater me sari true hogi to true hoga antha false return karata hai
print(x==10 and x<y and x==y)
# or operater me koi eke true hoga to true aye ga
print(x==10 or x<y or x==y)
print(x!=10 or x<y or x==y)
# not true false karata hai and false ko true karata hai
print(not x!=10 )